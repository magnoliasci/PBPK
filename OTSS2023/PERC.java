    public void initial()
    {
            if(pdose == 0.0)
            {
                ka = 0.0;
            }
            qc = qcc*(pow(bw, 0.75));
            qp = qpc*(pow(bw, 0.75));
            ql = qlc*qc;
            qf1 = qfc1*qc;
            qf2 = qfc2*qc;
            qs = ((qsc*qc)-qf1)-qf2;
            qr = (qrc*qc)-ql;
            vl = vlc*bw;
            vf1 = vfc1*bw;
            vf2 = vfc2*bw;
            vs = ((vsc*bw)-vf1)-vf2;
            vr = (vrc*bw)-vl;
            vmax = vmaxc*(pow(bw, 0.75));
            kf = kfc/(pow(bw, 0.25));
            drink = (pdrink*bw)/24.0;
            dose = pdose*bw;
            ivr = (ivdose*bw)/tinf;
            total = 0.0;
            tdose = 0.0;
            day = 0.5;
            con = 0.0;
            stocht = mwt/mw;
            ku = kuc/(pow(bw, 0.25));
            vd = vdc*bw;
    }

